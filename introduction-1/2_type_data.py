# integer
a = 10
print(a)
print(type(a))

# string
ab = "saya adalah string"
print(ab)
print(type(ab))

# float
ac = 4.5
print(ac)
print(type(ac))

# boolean
ad = True
ae = False

print(ad)
print(ae)
print(type(ad))

# array/list
af = [1, 4, 7, 8]
print(af)
print(type(af))

# object/dict
ag = {"nama": "anton", "umur": 25}
print(ag)
print(ag['nama'])
print(ag.get('umur'))
print(type(ag))